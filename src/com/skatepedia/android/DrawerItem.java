package com.skatepedia.android;

/**
 * Class drawerItem to display Activitys in Drawer
 */
public class DrawerItem {

    /** The name. */
    public String name;

    /**
     * Instantiates a new drawer item.
     *
     * @param name
     *            the name of the Activity
     */
    public DrawerItem(String name) {
	this.name = name;
    }
}
